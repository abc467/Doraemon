        inline void Filterate760::repairAddingPoints(OnePointInfoManage760& points, uint16_t operate_frequency)
        {
             if (operate_frequency != 10) // 暂时不处理 0.3分辨率
                return;
            // 0-799 只遍历这些角度上的点
            const int check_angle[] = { 99,100,199,200,299,300,399,400,499,500,599,600,699,700,799 };

            for (int angle : check_angle)
            {
                if (angle % 2 == 0)
                {
                    // 偶数角度 处理 低96线
                    for (int line = 97; line <= 192; line++)
                    {
                        // 取出左边一个点和右边一个点
                        auto& pre_point = points.getData(1, line, angle - 1);
                        auto& own_point = points.getData(1, line, angle);
                        auto& next_point = points.getData(1, line, angle + 1);
                        // 左右点在同一个位置 则 按均值补点
                        if (own_point.distance < 40 && std::abs((int)pre_point.distance - (int)next_point.distance) < 0.5)
                        {
                            own_point.distance = (pre_point.distance + next_point.distance) / 2;
                            own_point.non_filterable = 7;
                            // own_point.calculatexyz();
                            uint32_t azimuth = (uint32_t)(own_point.hor_angle * 1e3 + 360000) % 360000;
                            uint32_t elevation = (uint32_t)(own_point.ver_angle * 1e3 + 360000) % 360000;
                            float xy = (float)own_point.distance * COS(elevation);
                            own_point.x = (float)(xy * COS(azimuth));
                            own_point.y = (float)(xy * SIN(azimuth));
                            own_point.z = (float)(own_point.distance * SIN(elevation));
                        }
                    }
                }
                else
                {
                    // 奇数角度 处理 高96线
                    for (int line = 1; line <= 96; line++)
                    {
                        auto& pre_point = points.getData(1, line, angle - 1);
                        auto& own_point = points.getData(1, line, angle);
                        auto& next_point = points.getData(1, line, angle + 1);
                        // 左右点在同一个位置 则 按均值补点
                        if (own_point.distance < 40 && std::abs((int)pre_point.distance - (int)next_point.distance) < 0.5)
                        {
                            own_point.distance = (pre_point.distance + next_point.distance) / 2;
                            own_point.non_filterable = 7;
                            // own_point.calculatexyz();
                            uint32_t azimuth = (uint32_t)(own_point.hor_angle * 1e3 + 360000) % 360000;
                            uint32_t elevation = (uint32_t)(own_point.ver_angle * 1e3 + 360000) % 360000;
                            float xy = (float)own_point.distance * COS(elevation);
                            own_point.x = (float)(xy * COS(azimuth));
                            own_point.y = (float)(xy * SIN(azimuth));
                            own_point.z = (float)(own_point.distance * SIN(elevation));
                        }
                    }
                }
            }
        }
